#!/usr/bin/env python3
# Minimal Luau bundler for react-lua -> single loadstring-able file.
# Stores each reachable module's source and lazily loadstring()s it with an
# injected require(). Lazy execution => in-function cyclic requires resolve
# exactly like Roblox. export type stays legal (each module is its own chunk).
import os, re, sys

ROOT = os.path.abspath('.')

RE_STR    = re.compile(r'require\(\s*["\']([^"\']+)["\']\s*\)')
# require(script.Parent.Foo), require(script.Parent["Foo.new"]), multi-line ok
RE_SCRIPT = re.compile(
    r'require\(\s*script('
    r'(?:\s*\.\s*Parent\b|\s*\.\s*[A-Za-z_][A-Za-z0-9_]*|\s*\[\s*"[^"]+"\s*\]|\s*\[\s*\'[^\']+\'\s*\])+'
    r')\s*\)', re.S)
RE_SEG = re.compile(
    r'\.\s*Parent\b|\.\s*([A-Za-z_][A-Za-z0-9_]*)|\[\s*"([^"]+)"\s*\]|\[\s*\'([^\']+)\'\s*\]')

def read(p):
    with open(p, encoding='utf-8') as f:
        return f.read()

def modid(abspath):
    return os.path.relpath(abspath, ROOT).replace('\\', '/')

def resolve_file(cand):
    for p in (cand, cand + '.luau', cand + '.lua',
              os.path.join(cand, 'init.luau'), os.path.join(cand, 'init.lua')):
        if os.path.isfile(p):
            return os.path.abspath(p)
    return None

def resolve_str(fromfile, req):
    if req.startswith('./') or req.startswith('../'):
        cand = os.path.normpath(os.path.join(os.path.dirname(fromfile), req))
    elif req.startswith('@pkg/'):
        cand = os.path.normpath(os.path.join(ROOT, 'node_modules', req[5:]))
    else:
        return None
    return resolve_file(cand)

def resolve_script(fromfile, chain):
    node = fromfile
    for m in RE_SEG.finditer(chain):
        name = m.group(1) or m.group(2) or m.group(3)
        node = os.path.dirname(node) if name is None else os.path.join(node, name)
    return resolve_file(node)

def strip_comments(src):
    src = re.sub(r'--\[(=*)\[.*?\]\1\]', '', src, flags=re.S)   # block comments
    out = []
    for line in src.split('\n'):
        i = line.find('--')
        out.append(line if i < 0 else line[:i])                  # naive line comment
    return '\n'.join(out)

def rewrite(fromfile, src):
    def sub_str(m):
        tgt = resolve_str(fromfile, m.group(1))
        return 'require("%s")' % modid(tgt) if tgt else m.group(0)
    def sub_script(m):
        tgt = resolve_script(fromfile, m.group(1))
        return 'require("%s")' % modid(tgt) if tgt else m.group(0)
    src = RE_SCRIPT.sub(sub_script, src)
    src = RE_STR.sub(sub_str, src)
    return src

def deps(fromfile):
    scan = strip_comments(read(fromfile))
    found = set()
    for m in RE_STR.finditer(scan):
        t = resolve_str(fromfile, m.group(1))
        if t: found.add(t)
    for m in RE_SCRIPT.finditer(scan):
        t = resolve_script(fromfile, m.group(1))
        if t: found.add(t)
    return found

def bundle(entry_rel, out_rel):
    entry = os.path.abspath(entry_rel)
    seen, order, stack = set(), [], [entry]
    while stack:
        f = stack.pop()
        if f in seen:
            continue
        seen.add(f); order.append(f)
        for d in deps(f):
            if d not in seen:
                stack.append(d)

    sources = {modid(f): rewrite(f, read(f)) for f in order}

    # pick a long-bracket level that collides with no source
    level = 1
    while any((']' + '=' * level + ']') in s for s in sources.values()):
        level += 1
    ob, cb = '[' + '=' * level + '[', ']' + '=' * level + ']'

    parts = ['-- Syphon react-lua bundle (auto-generated by build/bundle.py). Do not edit.',
             'local Sources = {}']
    for mid, src in sources.items():
        parts.append('Sources[%r] = %s\n%s\n%s' % (mid, ob, src, cb))
    parts.append(r'''
local Cache, Building = {}, {}
local function require(id)
    local c = Cache[id]
    if c ~= nil then return c end
    if Building[id] then error('Syphon bundle: cyclic require ' .. id, 2) end
    local src = Sources[id]
    if not src then error('Syphon bundle: missing module ' .. tostring(id), 2) end
    Building[id] = true
    local fn = assert(loadstring(src, '=@' .. id))
    setfenv(fn, setmetatable({ require = require }, { __index = getfenv() }))
    local r = fn()
    if r == nil then r = true end
    Cache[id], Building[id] = r, nil
    return r
end
''')
    parts.append('return require(%r)' % modid(entry))

    os.makedirs(os.path.dirname(out_rel), exist_ok=True)
    with open(out_rel, 'w', encoding='utf-8', newline='\n') as f:
        f.write('\n'.join(parts))
    print('bundled %d modules -> %s (%d KB, bracket level %d)'
          % (len(order), out_rel, os.path.getsize(out_rel) // 1024, level))

if __name__ == '__main__':
    bundle(sys.argv[1], sys.argv[2])
